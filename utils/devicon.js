function devicon (name, isDirectory) {
    if (isDirectory)
        return ''

    let exact = file_node_exact_matches[name]
    if (exact)
        return exact
    
    exact = file_node_extensions[path.extname(name)]
    if (exact)
        return exact

    return ''
}

